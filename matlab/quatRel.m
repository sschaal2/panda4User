function q=quatRel(qf,qs)

  Q(1,1) =  qf(1);
  Q(1,2) = -qf(2);
  Q(1,3) = -qf(3);
  Q(1,4) = -qf(4);

  Q(2,1) =  qf(2);
  Q(2,2) =  qf(1);
  Q(2,3) =  qf(4);
  Q(2,4) = -qf(3);

  Q(3,1) =  qf(3);
  Q(3,2) = -qf(4);
  Q(3,3) =  qf(1);
  Q(3,4) =  qf(2);

  Q(4,1) =  qf(4);
  Q(4,2) =  qf(3);
  Q(4,3) = -qf(2);
  Q(4,4) =  qf(1);
  
  qs(2:4) = -qs(2:4);
  
  q = Q*qs;